        tabela.setNumRows(0);
        
        Session sesion = HibernateUtil.getSessionFactory().openSession();
        Transaction tx = null;
        SQLQuery query;
        query = sesion.createSQLQuery("SELECT*FROM mensalidade where idCliente =" + id);
        query.setResultTransformer(Criteria.ALIAS_TO_ENTITY_MAP);
        java.util.List results = query.list();
        for (Object object : results) {
            Map row = (Map) object;
            tmResultSet.addRow(new Object[]{row.get("id"),row.get("janeriro"),
                row.get("fevereiro"), row.get("marco"), row.get("abril"),
                row.get("maio"), row.get("junho"), row.get("julho"), row.get("agosto")
                    , row.get("setembro"), row.get("outubro"), row.get("novembro"), row.get("dezembro")
                    
             });
            
        }    
