    public void salvar(){
        Cliente cliente = new Cliente();
        cliente.setNome(jTNome.getText());
        cliente.setApelido(jTApelido.getText());
        cliente.setCpf(jFCPF.getText());
        cliente.setTelefone(jFTelefone.getText());
        cliente.setDataNascimento(jFDataNascimento.getText());
        cliente.setLogradouro(jTLogradouro.getText());
        cliente.setNumero(Integer.parseInt(jTNumero.getText()));
        cliente.setEstado(jCEstado.getSelectedItem().toString());
        cliente.setBairro(jTBairro.getText());
        cliente.setCidade(jTMunicipio.getText());
        cliente.setIdMatricula(Integer.parseInt(jTMatricula.getText()));
        
        Session session = HibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
        session.save(cliente);
        session.getTransaction().commit();
        JOptionPane.showMessageDialog(this, "Cadastrado com Sucesso.");
        
    }
